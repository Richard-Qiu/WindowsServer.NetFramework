        private static bool IsChineseIpAddressInternal(uint ipV4, int ipIndex)
        {
            return (ipIndex & 1) == 0;
        }

        public static bool IsChineseIpAddress(uint ipV4)
        {
            int start = 0;
            int end = _chineseIpRanges.Length - 1;

            int current = 0;
            while (end > start)
            {
                current = (end + start) / 2;
                if (ipV4 < _chineseIpRanges[current])
                {
                    end = current;
                }
                else if (_chineseIpRanges[current + 1] <= ipV4)
                {
                    start = current + 1;
                }
                else
                {
                    return IsChineseIpAddressInternal(ipV4, current);
                }
            }

            return IsChineseIpAddressInternal(ipV4, start);
        }

        public static bool IsChineseIpAddress(IPAddress ipV4)
        {
            return IsChineseIpAddress(ConvertToIpV4UInt32(ipV4));
        }

        public static IPAddress ConvertToIpAddress(uint ipV4)
        {
            byte[] b = new byte[4]
            {
                (byte)((ipV4 >> 24 ) & 0xFF),
                (byte)((ipV4 >> 16 ) & 0xFF),
                (byte)((ipV4 >> 8 ) & 0xFF),
                (byte)(ipV4 & 0xFF),
            };
            return new IPAddress(b);
        }

        public static uint ConvertToIpV4UInt32(IPAddress ipV4)
        {
            if (ipV4.AddressFamily != System.Net.Sockets.AddressFamily.InterNetwork)
            {
                throw new ArgumentOutOfRangeException("ipV4", "Its AddressFamily must be AddressFamily.InterNetwork.");
            }

            byte[] bytes = ipV4.GetAddressBytes();
            if (bytes.Length != 4)
            {
                throw new ArgumentOutOfRangeException("ipV4", "The length of address bytes is not 4.");
            }

            uint address = (((uint)bytes[0]) << 24) | (((uint)bytes[1]) << 16) | (((uint)bytes[2]) << 8) | ((uint)bytes[3]);
            return address;
        }

        public static Guid ConvertToGuid(IPAddress ip)
        {
            if ((ip.AddressFamily != System.Net.Sockets.AddressFamily.InterNetwork)
                && (ip.AddressFamily != System.Net.Sockets.AddressFamily.InterNetworkV6))
            {
                throw new ArgumentOutOfRangeException("ipV4", "Its AddressFamily must be AddressFamily.InterNetwork v4 or v6.");
            }

            byte[] ipBytes = ip.GetAddressBytes();
            byte[] guidBytes = new byte[16];
            Array.Copy(ipBytes, guidBytes, Math.Min(ipBytes.Length, guidBytes.Length));
            return new Guid(guidBytes);
        }

        public static Guid ConvertToGuid(string ip, Guid defaultGuid)
        {
            if (!string.IsNullOrEmpty(ip))
            {
                IPAddress address;
                if (IPAddress.TryParse(ip, out address))
                {
                    return ConvertToGuid(address);
                }
            }
            return defaultGuid;
        }
